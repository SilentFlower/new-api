// GetLogChartDataByKey 按 API Key（token_id）查询图表数据（公共日志查看器图表）
// 返回模型调用分布（饼图）和消耗趋势（折线图）数据
func GetLogChartDataByKey(c *gin.Context) {
	tokenId := c.GetInt("token_id")
	if tokenId == 0 {
		common.ApiErrorMsg(c, "无效的令牌")
		return
	}

	startTimestamp, _ := strconv.ParseInt(c.Query("start_timestamp"), 10, 64)
	endTimestamp, _ := strconv.ParseInt(c.Query("end_timestamp"), 10, 64)

	// 时间跨度限制 1 个月
	if endTimestamp-startTimestamp > 2592000 {
		common.ApiErrorMsg(c, "时间跨度不能超过 1 个月")
		return
	}

	// 查询模型调用分布数据（饼图）
	modelStats, err := model.GetTokenModelStats(tokenId, startTimestamp, endTimestamp)
	if err != nil {
		common.ApiError(c, err)
		return
	}

	// 查询消耗趋势数据（折线图）
	quotaData, err := model.GetTokenQuotaData(tokenId, startTimestamp, endTimestamp)
	if err != nil {
		common.ApiError(c, err)
		return
	}

	common.ApiSuccess(c, gin.H{
		"model_stats": modelStats,
		"quota_data":  quotaData,
	})
}

